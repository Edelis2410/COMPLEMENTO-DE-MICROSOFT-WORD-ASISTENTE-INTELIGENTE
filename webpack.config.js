/* eslint-disable no-undef */

const devCerts = require("office-addin-dev-certs");
const CopyWebpackPlugin = require("copy-webpack-plugin");
const HtmlWebpackPlugin = require("html-webpack-plugin");
const MiniCssExtractPlugin = require("mini-css-extract-plugin"); // <--- 1. IMPORTAR EL EXTRACTOR DE CSS

const urlDev = "https://localhost:3000/";
const urlProd = "https://www.contoso.com/"; 

async function getHttpsOptions() {
  const httpsOptions = await devCerts.getHttpsServerOptions();
  return { ca: httpsOptions.ca, key: httpsOptions.key, cert: httpsOptions.cert };
}

module.exports = async (env, options) => {
  const dev = options.mode === "development";
  const config = {
    devtool: "source-map",
    entry: {
      polyfill: ["core-js/stable", "regenerator-runtime/runtime"],
      taskpane: ["./src/taskpane/taskpane.js", "./src/taskpane/taskpane.html"],
      commands: "./src/commands/commands.js",
    },
    output: {
      clean: true,
    },
    resolve: {
      extensions: [".html", ".js", ".css"],
    },
    module: {
      rules: [
        {
          test: /\.js$/,
          exclude: /node_modules/,
          use: {
            loader: "babel-loader",
          },
        },
        {
          test: /\.html$/,
          exclude: /node_modules/,
          use: "html-loader",
        },
        {
          test: /\.css$/, 
          exclude: /node_modules/,
          // <--- 2. Reemplazamos "style-loader" por el cargador del extractor físico
          use: [MiniCssExtractPlugin.loader, "css-loader"], 
        },
        {
          test: /\.(png|jpg|jpeg|gif|ico)$/,
          type: "asset/resource",
          generator: {
            filename: "assets/[name][ext][query]",
          },
        },
      ],
    },
    plugins: [
      // <--- 3. PLUGIN: Extrae el CSS compilado a un archivo independiente enlazado directamente al <head>
      new MiniCssExtractPlugin({
        filename: "taskpane.css",
      }),
      new HtmlWebpackPlugin({
        filename: "taskpane.html",
        template: "./src/taskpane/taskpane.html",
        chunks: ["polyfill", "taskpane"],
      }),
      new CopyWebpackPlugin({
        patterns: [
          {
            from: "assets/*",
            to: "assets/[name][ext][query]",
          },
          {
            from: "manifest*.xml",
            to: "[name]" + "[ext]",
            transform(content) {
              if (dev) {
                return content;
              } else {
                return content.toString().replace(new RegExp(urlDev, "g"), urlProd);
              }
            },
          },
        ],
      }),
      new HtmlWebpackPlugin({
        filename: "commands.html",
        template: "./src/commands/commands.html",
        chunks: ["polyfill", "commands"],
      }),
    ],
    devServer: {
      headers: {
        "Access-Control-Allow-Origin": "*",
      },
      server: {
        type: "https",
        options: {
          key: require("fs").readFileSync("C:\\Users\\Win 11\\.office-addin-dev-certs\\localhost.key"),
          cert: require("fs").readFileSync("C:\\Users\\Win 11\\.office-addin-dev-certs\\localhost.crt"),
          ca: require("fs").readFileSync("C:\\Users\\Win 11\\.office-addin-dev-certs\\ca.crt")
        }
      },
      port: process.env.npm_package_config_dev_server_port || 3000,
    },
  };

  return config;
};
